export const getLPTokenName = (tokenSymbol: string): string => {
  // For FAKFUN, use the full name with -LP suffix
  if (tokenSymbol === "FAKFUN") {
    return "sBTC-FAKFUN-LP";
  }

  // For all other tokens, use sBTC-{SYMBOL}-LP format
  return `sBTC-${tokenSymbol}`;
};
